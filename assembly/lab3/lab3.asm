DSEG SEGMENT
DATA DB 100, 110, 120
DSEG ENDS

SSEG SEGMENT STACK
SKTOP DB 20 DUP(0)
SSEG ENDS

CSEG SEGMENT
    ASSUME CS:CSEG, DS:DSEG, SS:SSEG
START: 
    MOV AX, DSEG ;初始化
    MOV DS, AX
    MOV AX, SSEG
    MOV SS, AX
    
FIRST:
    CMP [DATA], 0
    JLE SECOND
    CMP [DATA + 1], 0
    JLE SECOND
    CMP [DATA + 2], 0
    JLE SECOND
    MOV DL, 2BH
    MOV AH, 02H
    INT 21H
    JMP RESULT

SECOND:
    CMP [DATA], 0
    JGE THIRD
    CMP [DATA + 1], 0
    JGE THIRD
    CMP [DATA + 2], 0
    JGE THIRD
    MOV DL, 2DH
    MOV AH, 02H
    INT 21H
    JMP RESULT

THIRD:
    MOV DL, 20H
    MOV AH, 02H
    INT 21H

RESULT:
    MOV AH, 4CH
    MOV AL, 0
    INT 21H

CSEG ENDS
END START
